    /// <summary>
    /// Логика взаимодействия для AuthWindow.xaml
    /// </summary>
    public partial class AuthWindow : Window
    {
        public AuthWindow()
        {
            InitializeComponent();
        }

        private void Button_Auth_Click(object sender, RoutedEventArgs e)
        {
            string login = textBoxLogin.Text.Trim();
            string pass = passBox.Password.Trim();

            if (login.Length < 5)
            {
                textBoxLogin.ToolTip = "Це поле введено не коректно!";
                textBoxLogin.Background = Brushes.DarkRed;
            }
            else if (pass.Length < 5)
            {
                passBox.ToolTip = "Це поле введено не коректно!";
                passBox.Background = Brushes.DarkRed;
            }
            else
            {
                textBoxLogin.ToolTip = "";
                textBoxLogin.Background = Brushes.Transparent;
                passBox.ToolTip = "";
                passBox.Background = Brushes.Transparent;

                User authUser = null;

                using (ApplicationContext bd = new ApplicationContext())
                {
                    authUser = bd.Users.Where(user => user.Login == login && user.Pass == pass).FirstOrDefault();

                }
                if (authUser != null) 
                {
                    MessageBox.Show("SO GOOD");
                    UserPageWindow userPageWindow = new UserPageWindow(textBoxLogin.Text);
                    userPageWindow.Show();
                    Hide();

                }
                else
                {
                    MessageBox.Show("No CORECT login or pass");
                }
                    
            }
        }

        private void Button_Window_Reg_Click(object sender, RoutedEventArgs e)
        {
            MainWindow mainWindow = new MainWindow();
            mainWindow.Show();
            Hide();
        }
    }
}
